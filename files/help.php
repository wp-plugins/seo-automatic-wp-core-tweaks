<h3>RSS Extended:</h3>


<p>This is available under Design &gt; Widgets as &quot;RSS(Extended).</p>


<p>The features added to the built-in RSS Widget function are:</p>
<ol>
	<li>The ability to have the feed links open in a new tab/window.</li>
	<li>Add &quot;nofollow&quot; tags to the feed links.</li>
</ol>


<h3>H1 and H2 Tag Changing:</h3>


<p>To use this feature, you must first change the <b>the_title()</b> tag in your page.php and/or single.php template to <b>single_post_title()</b>.<br>
<br />
Then use <b>changeH1</b> as the key name for the words that you want to display as the <b><font color="#0000FF">body heading</font></b>.
<font color="#FF0000"><b>The browser title and the page 
name in the menu will not be affected, they will continue to display the original title.</b></font></p>
